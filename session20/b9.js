// Khai báo biến a là số nguyên
let a = 5;

// Khai báo biến b là chuỗi
let b= "5";

// Kiểm tra giá trị của a và b có bằng nhau không
console.log(a==b); // true, vì a và b đều có giá trị là 5

// Kiểm tra giá trị của a và b có bằng nhau cả kiểu dữ liệu không
console.log(a===b); // false, vì a là số nguyên, còn b là chuỗi

// Kiểm tra giá trị của a và b có khác nhau không
console.log(a!=b); // true, vì a và b không bằng nhau

// Kiểm tra giá trị của a và b có khác nhau cả kiểu dữ liệu không
console.log(a!==b); // true, vì a và b không cùng kiểu dữ liệu